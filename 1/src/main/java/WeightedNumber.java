public class WeightedNumber {
    private double number;
    private int weight;

    public WeightedNumber(double number, int weight) {
        this.number = number;
        this.weight = weight;
    }

    public double getNumber() {
        return number;
    }

    public void setNumber(double number) {
        this.number = number;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }
}
